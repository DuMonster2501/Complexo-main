-- STOPANIMHOTWIRED
-----------------------------------------------------------------------------------------------------------------------------------------
function cnVRP.stopAnimHotwired(vehicle)
	while not HasAnimDictLoaded(animDict) do
		RequestAnimDict(animDict)
		Citizen.Wait(10)
	end
	vehHotwired = false
	StopAnimTask(PlayerPedId(),animDict,anim,2.0)
	SetEntityAsMissionEntity(vehicle,true,true)
end
