-----------------------------------------------------------------------------------------------------------------------------------------
-- VRP
-----------------------------------------------------------------------------------------------------------------------------------------
local Tunnel = module("vrp","lib/Tunnel")
local Proxy = module("vrp","lib/Proxy")
local Tools = module("vrp","lib/Tools")
vRP = Proxy.getInterface("vRP")
vRPclient = Tunnel.getInterface("vRP")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
cnVRP = {}
Tunnel.bindInterface("notepad",cnVRP)
vCLIENT = Tunnel.getInterface("notepad")
local idgens = Tools.newIDGenerator()
-----------------------------------------------------------------------------------------------------------------------------------------
-- VARIABLES
-----------------------------------------------------------------------------------------------------------------------------------------
local notePad = {}
-----------------------------------------------------------------------------------------------------------------------------------------
-- CREATENOTEPAD
-----------------------------------------------------------------------------------------------------------------------------------------
function cnVRP.createNotepad(text)
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		local id = idgens:gen()
		local x,y,z = vRPclient.getPositions(source)
		notePad[id] = { id = id, text = tostring(text), x = x, y = y, z = z }
		TriggerClientEvent("notepad:sendPlayers",-1,id,notePad[id])
	end
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- EDITNOTEPAD
-----------------------------------------------------------------------------------------------------------------------------------------
function cnVRP.editNotepad(id,text)
	if notePad[id] then
		notePad[id].text = tostring(text)
		TriggerClientEvent("notepad:sendPlayers",-1,id,notePad[id])
	end
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- DESTROYNOTEPAD
-----------------------------------------------------------------------------------------------------------------------------------------
function cnVRP.destroyNotepad(id)
	if notePad[id] then
		idgens:free(id)
		notePad[id] = nil
		TriggerClientEvent("notepad:removePlayers",-1,id)
	end
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- PLAYERSPAWN
-----------------------------------------------------------------------------------------------------------------------------------------
AddEventHandler("vRP:playerSpawn",function(user_id,source)
	vCLIENT.updateNotepad(source,notePad)
end)